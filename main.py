def main():
    # Take input from the user for two numbers
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))

    # Calculate the sum
    result = num1 + num2

    # Display the result
    print("The sum of", num1, "and", num2, "is:", result)

if __name__ == "__main__":
    main()
